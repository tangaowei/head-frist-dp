public class GarageDoor {
    public void up() {
	System.out.println("GarageDoor Up");
    }

    public void down() {
	System.out.println("GarageDoor Down");
    }

    public void stop() {
	System.out.println("GarageDoor Stop");
    }

    public void lightOn() {
	System.out.println("Garage Door Light On");
    }

    public void lightOff() {
	System.out.println("Garage Door Light Off");
    }
}
